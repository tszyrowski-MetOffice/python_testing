def number_to_power(number, power):
    '''
    :param number: number to be taken 
    :param power: 
    :return: number to power
    
    >>> number_to_power(3, 2)
    9
    >>> number_to_power(2, 3)
    8
    '''
    return number**power

def number_addition(number1, number2):
    return number1 + number2

def number_substract(number1, number2):
    return number1 - number2
